async function createListing(gfapi, query, delay) {
  return new Promise(resolve => {
    setTimeout(async () => {
      try {
        console.log(`Creating listing: ${query.name}`);
        let listing = await gfapi.listing_post(query);

        // Subir im√°genes
let photo_url = getImageUrl(query.name);
await gfapi.upload_photo(listing.id, photo_url, 0); // Imagen de la p√°gina de listado

        // Poner el listado en venta
        await gfapi.listing_status(listing.id, GfApi.LISTING_STATUS.ONSALE);
        console.log(`Listing created and set to ON SALE: ${query.name}`);
        resolve();
      } catch (err) {
        console.error(`Error creating listing: ${query.name}`, err);
        resolve(); // Continuar con el siguiente listado aunque haya un error
      }
    }, delay);
  });
}

